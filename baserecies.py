# ШАГ 1:Как запечь мясо по-французски с сыром и помидорами в духовке?Подготовьте необходимые ингредиенты. Выбирайте более жирную часть свинины - лопатку или шею. Также можно использовать любое другое мясо. Но помните, что время готовки изменится. Например, говядина будет запекаться дольше, чем свинина, а куриное филе или индейка меньше. Помидоры выбирайте спелые, сочные и плотные. Слишком мягкие плоды будет неудобно нарезать. Включите духовку на 180 градусов.
# ШАГ 2:Мясо тщательно помойте под проточной водой и обсушите бумажными полотенцами. При необходимости с помощью ножа уберите сухожилия и пленки. Поперек волокон нарежьте кусок тонкими широкими ломтиками толщиной около 1 см. Выложите мясо на разделочную доску. Накройте полиэтиленовым пакетом или пищевой пленкой. Это нужно для того, чтобы при отбивании брызги мясного сока не разлетались во все стороны. Кухонным молоточком отбейте каждый кусок с обеих сторон.
# ШАГ 3:Уберите пленку. Отбивные посолите, поперчите. Посыпьте паприкой. Паприку можно заменить готовыми специями, предназначенными для свинины. Или подберите специи по своему вкусу. Подойдут прованские травы, базилик, майоран, орегано. Если вы располагаете временем, отправьте мясо в холодильник на пару часов, пусть промаринуется. Так получится еще вкуснее.
# ШАГ 4:Форму для запекания смажьте небольшим количеством оливкового или подсолнечного рафинированного масла. В один слой выложите свиные отбивные.
# ШАГ 5:Лук очистите от шелухи. Тонко нарежьте его полукольцами. Для того, чтобы лук не щипал глаза, промойте его и нож холодной водой. Если вместо репчатого лука использовать белый или Ялтинский, вкус блюда приобретет сладковатую нотку и совсем не будет горчить. Выложите луковые кольца на мясо.
# ШАГ 6:Помойте помидоры и высушите их бумажными полотенцами. При необходимости удалите у них хвостики. Острым ножом нарежьте томаты тонкими кольцами. Разложите их поверх лука. Свежие овощи можно заменить томатным соусом или вообще исключить из рецептуры. Но с ними будет вкуснее и сочнее.
# ШАГ 7:Слегка смажьте слой из помидоров майонезом. Если по каким-то причинам майонез вам не нравится, замените его на сметану.
# ШАГ 8:На крупной терке натрите твердый сыр. Щедро посыпьте им слой из помидоров. Чем больше сыра вы добавите, тем вкуснее будет. Можно использовать такие сорта, как Российский, Эдам или Адыгейский. Главное, чтобы продукт был качественным, вкусным и ароматным. В этом блюде сыр должен быть запеченным, потому лучше добавьте его сразу. Но если хотите, чтобы он просто расплавился, добавьте за 15 минут до конца готовки.
# ШАГ 9:Покройте сыр густой майонезной сеточкой.
# ШАГ 10:Запекайте мясо по-французски около 40-50 минут при 180 градусах. Чтобы мясо лучше пропеклось, а сыр не сгорел, накройте форму фольгой. За 15-20 минут до конца снимите ее. Запекайте до образования аппетитной сырной корочки. Как проверить готовность блюда? Проткните острием ножа посередине. Если будет вытекать прозрачный сок, значит готово. Если сок будет розоватым, готовьте еще 10 минут. Приятного аппетита!

# ПЫШНАЯ ТВОРОЖНАЯ ЗАПЕКАНКА В ДУХОВКЕ
# Творог:600
# Сметана:150
# Яйца:180
# Сахар:80
# Манка:56
# Сода пищевая:7.5
# Ванилин:1.3
# ШАГ 1:Как сделать пышную творожную запеканку в духовке? Подготовьте продукты. Творог возьмите пожирнее, минимум 5%. Тогда запеканка получится действительно вкусной, не сухой. Сметану также выбирайте пожирнее. Если у вас густая сметана, то положите ее поменьше, жидкую - побольше. Количество сахара можете менять по своему вкусу - 4 ложки - среднее количество.
# ШАГ 2:Возьмите миску, положите в нее творог. В этом рецепте все ингредиенты пробиваются погружным блендером. Если у вас его нет, то протрите творог через сито - он станет однородным. Добавьте сахар. Всыпьте соду. Она необходима для пышности запеканки. И соль, для усиления вкуса. Положите ванилин для аромата. Как выбрать творог и особенности использования соды вы узнаете прочитав статьи в конце этого рецепта.
# ШАГ 3:Разбейте к творогу яйца. Их возьмите крупные, отборной категории. Если яйца мельче, увеличьте их количество. Они также отвечают за пышность запеканки, не уменьшайте их количество. Если у вас нет блендера, то яйца взбейте с сахаром отдельно, и только затем добавьте к основной массе. Положите сметану. Учитывайте, какой у вас творог - сухой или влажный - и регулируйте количество сметаны.
# ШАГ 4:Пробейте все ингредиенты погружным блендером - масса станет однородной и кремовой.
# ШАГ 5:Добавьте манную крупу. Ее количество также может быть разной. Если изначально масса была густой - положите меньше, если слишком жидкой - больше на одну ложку. Учитывайте, что она еще набухнет при выпечке. Не переборщите.
# ШАГ 6:Хорошо перемешайте массу. Оставьте ее постоять на 20 минут, за это время манка набухнет.
# ШАГ 7:Подготовьте форму для выпечки. Смажьте ее кусочком мягкого сливочного масла, а затем присыпьте манкой. Манка прилипнет к маслу и и при выпечке станет корочкой. Это поможет легко вынуть запеканку из формы.
# ШАГ 8:Вылейте в форму тесто, разровняйте верх лопаткой. Моя форма размером 20*20 см, запеканка получилась не очень высокой. В меньшей форме она получится выше.
# ШАГ 9:Запекайте запеканку в духовке при температуре 180°С, режим верх-низ, около 30-40 минут, до румяной верхушки. Время указано приблизительно. О работе духовок читайте под шагами. Как проверить, готова ли запеканка? Как только на поверхности образуется легкая золотистая корочка, возьмите шпажку и проткните запеканку в нескольких местах. Если шпажка сухая, то запеканка готова. Готовую запеканку полностью остудите и только потом вынимайте из формы.

# СВИНИНА В ДУХОВКЕ С СЫРОМ И ПОМИДОРАМИ
# Свинина:600
# Твёрдый сыр:200
# Помидоры:300
# Сметана:80
# Чеснок:10
# Петрушка:10
# Растительное масло:10
# ШАГ 1:Как сделать свинину в духовке с сыром и помидорами? Подготовьте для этого необходимые ингредиенты. Свинину выбирайте без костей. Удобно использовать свиной окорок или карбонат. Сметана может быть любой жирности. Сыр также выбирайте на свой вкус.
# ШАГ 2:Свинину нарежьте поперек волокон на порционные кусочки толщиной, примерно, по 1-1,5 см. и немного отбейте кухонным молоточком.
# ШАГ 3:Для запекания подойдет любая жаропрочная форма. Смажьте её растительным маслом и выложите кусочки отбитого мяса. Посыпьте солью и специями. В качестве специи достаточно будет молотого перца.
# ШАГ 4:Сыр натрите на терке. Немного отложите отдельно для посыпки. Петрушку мелко нарежьте. Чеснок очистите и пропустите через пресс. Смешайте сыр, сметану, петрушку и чеснок.
# ШАГ 5:Получившуюся сырную массу равномерно намажьте по подготовленным кусочкам мяса.
# ШАГ 6:Помидоры помойте и нарежьте кружочками. Разложите нарезанные помидоры по мясу.
# ШАГ 7:Сверху посыпьте отложенным тертым сыром. поставьте мясо в духовку, предварительно разогретую до 180 градусов, запекайте около 35-40 минут. Время указано приблизительно, ориентируйтесь на свою духовку. Подробнее о работе духовок читайте в отдельной статье в конце рецепта..
# ШАГ 8:За это время в форме появится сок и часть его успеет испариться. Сыр не только расплавится, но и немного подрумянится. Готовое мясо извлеките из духовки и подавайте к столу в горячем виде. Приятного аппетита!

