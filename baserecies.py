# ШАГ 2:Очищенные картофель и лук натрите на терке. Для более однородной консистенции используйте мелкую терку. Я натираю на крупной. Это уже дело вкуса.
# ШАГ 3:К тертым овощам добавьте яйцо, муку, соль и специи. В качестве специй рекомендую использовать немного молотого черного перца и сухой укроп или петрушку.
