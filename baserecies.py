# ШАГ 6:Обжаривайте драники с двух сторон на среднем огне до аппетитного, золотистого цвета. Драники должны как следует прожариться. Готовые драники подавайте в горячем виде, дополнив блюдо сметаной или чесночным соусом. Приятного аппетита!

# ЖУЛЬЕН С КУРИЦЕЙ И ГРИБАМИ В ДУХОВКЕ
# Куриное филе:600
# Шампиньоны:300
# Сметана (15-20% жирности):400
# Твёрдый сыр:100
# Оливковое масло:34
# ШАГ 1:Подготовьте все необходимые ингредиенты для приготовления жульена. Для блюда лучше использовать свежие шампиньоны, так как замороженные содержат слишком много влаги и при обжарке в вашей сковороде поначалу будет слишком много воды, а после ее испарения объем грибов будет намного меньше, чем нужно. Поэтому если используете замороженные грибы, возьмите их с запасом, предварительно их разморозьте и отожмите.
# ШАГ 2:Шампиньоны вымойте, обсушите и нарежьте ломтиками. Если грибы мелкие, можете просто разрезать их пополам.
# ШАГ 3:Куриное филе вымойте, обсушите и порежьте небольшими кубиками.
# ШАГ 4:Сыр натрите на крупной терке. Сыр можете использовать любой по вкусу. Подойдет и обычный "Российский", и итальянская моцарелла.
# ШАГ 5:В сковороде разогрейте растительное масло, выложите курицу и обжарьте, помешивая, на сильном огне около 4 минут пока филе со всех сторон не покроется белой коркой.
# ШАГ 6:Когда вся курица сменит розовый оттенок на белый, добавьте в сковороду грибы и обжарьте все вместе, помешивая, 10 минут до готовности шампиньонов. Необязательно доводить все до появления золотистой корочки. Достаточно просто довести ингредиенты до готовности. Если в сковороде останется немного сока от грибов и курицы, будет даже лучше. Вместе со сметаной получится замечательный соус, не уступающий по вкусу классическому Бешамель.
# ШАГ 7:Вообще обычно для жульена курицу и грибы обжаривают до золотистой корочки по-отдельности, а в конце соединяют. Но в этом рецепте я решила поэкспериментировать и добавила грибы намного раньше. Курица и грибы выделяют каждый свои соки и тушатся в них. Таким образом все содержимое сковороды пропитывается ароматным курино-грибным соком. Блюдо получается сочнее, кусочки курицы нежнее и ароматнее. Уберите сковороду с огня.
# ШАГ 8:Влейте в сковороду сметану жирностью от 15 до 20%.
# ШАГ 9:Все посолите, поперчите и перемешайте.
# ШАГ 10:Разложите получившуюся массу по кокотницам или небольшим горшочкам для запекания.
# ШАГ 11:Посыпьте жульен тертым сыром и поставьте в разогретую до 180°С духовку на 5-7 минут или чуть дольше до появления на сыре золотистой корочки.
# ШАГ 12:Подавайте жульен горячим, посыпав рубленой зеленью. Приятного аппетита!







